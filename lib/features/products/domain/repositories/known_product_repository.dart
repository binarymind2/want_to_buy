import '../entities/known_product.dart';

/// Репозиторий известных товаров.
///
/// Это domain-слой.
///
/// Важно:
/// здесь мы описываем, ЧТО приложение умеет делать с известными товарами,
/// но не описываем, КАК именно они хранятся.
///
/// То есть этот интерфейс ничего не знает про Isar, SQLite, сервер или файлы.
abstract class KnownProductRepository {
  /// Следит за всеми известными товарами.
  ///
  /// Stream нужен, чтобы UI в будущем мог автоматически обновляться,
  /// когда в БД появится новый товар.
  Stream<List<KnownProduct>> watchAll();

  /// Получает все известные товары один раз.
  ///
  /// Это пригодится для диалога "Товары в БД" на экране настроек.
  Future<List<KnownProduct>> getAll();

  /// Ищет товар по его id.
  Future<KnownProduct?> findById(String id);

  /// Ищет товар по нормализованному названию.
  ///
  /// Например:
  /// - "Молоко"
  /// - "молоко"
  /// - "  МОЛОКО  "
  ///
  /// должны привести к одному normalizedName: "молоко".
  Future<KnownProduct?> findByNormalizedName(String normalizedName);

  /// Возвращает существующий товар или создаёт новый.
  ///
  /// Это главный метод для сценария добавления покупки.
  ///
  /// Логика:
  /// 1. Пользователь вводит название.
  /// 2. Мы нормализуем название.
  /// 3. Проверяем, есть ли такой товар в БД.
  /// 4. Если есть — возвращаем его.
  /// 5. Если нет — создаём новый KnownProduct.
  Future<KnownProduct> getOrCreateByName(String name);

  /// Ищет подсказки по введённому тексту.
  ///
  /// Например, пользователь ввёл "мо",
  /// а метод вернёт:
  /// - Молоко
  /// - Морковь
  Future<List<KnownProduct>> searchSuggestions(String query, {int limit = 10});

  /// Сохраняет известный товар.
  ///
  /// Используем для обновления товара, например когда нужно записать
  /// lastPurchasedAt после покупки.
  Future<void> save(KnownProduct product);

  /// Отмечает товар как купленный.
  ///
  /// Сам товар из БД не удаляется.
  /// Мы только обновляем lastPurchasedAt.
  Future<KnownProduct?> markPurchased(String productId);
}
