/// Подготавливает название товара для отображения пользователю.
///
/// Что делаем:
/// - убираем пробелы по краям;
/// - заменяем несколько пробелов внутри строки на один;
/// - НЕ меняем регистр букв.
///
/// Примеры:
/// "  Молоко  " -> "Молоко"
/// "молоко" -> "молоко"
/// "  Молоко     2%  " -> "Молоко 2%"
String formatProductName(String value) {
  return value.trim().replaceAll(RegExp(r'\s+'), ' ');
}

/// Подготавливает название товара для технического поиска и уникальности.
///
/// Что делаем:
/// - сначала форматируем имя так же, как для отображения;
/// - затем приводим к нижнему регистру.
///
/// Примеры:
/// "  Молоко  " -> "молоко"
/// "МОЛОКО" -> "молоко"
/// "  Молоко     2%  " -> "молоко 2%"
String normalizeProductName(String value) {
  return formatProductName(value).toLowerCase();
}
